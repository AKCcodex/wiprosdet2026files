package day2;

public class task4_grdade {
static int marks = 78;
public static void main(String[] args) {
	if(marks>=90) {
		System.out.println("Grade:A");
	}
	else if(marks>=80) {
		System.out.println("Grade:B");
	}
	else if(marks>=70) {
		System.out.println("Grade:C");
	}
	else if(marks>=60) {
		System.out.println("Grade:D");
	}
	else {
		System.out.println("Grade:F");
	}
}
}
